	document.addEventListener('DOMContentLoaded', function() {
		const form = document.getElementById('leadForm');
		const submitBtn = document.getElementById('submit-btn');
		const originalBtnText = submitBtn.innerHTML;
		
		// Phone number auto-formatting
		const phoneInput = document.getElementById('phone');
		phoneInput.addEventListener('input', function(e) {
			let value = e.target.value.replace(/\D/g, '');
			if (value.length > 10) value = value.slice(0, 10);
			if (value.length >= 6) {
				value = '(' + value.slice(0,3) + ') ' + value.slice(3,6) + '-' + value.slice(6);
			} else if (value.length >= 3) {
				value = '(' + value.slice(0,3) + ') ' + value.slice(3);
			}
			e.target.value = value;
		});
		
		// Form submission handler
		form.addEventListener('submit', async function(e) {
			e.preventDefault();
			console.log('üìã Form submitted');
			
			submitBtn.disabled = true;
			submitBtn.innerHTML = '‚è≥ Processing...';
			
			const formData = {
				firstName: document.getElementById('firstName').value.trim(),
				lastName: document.getElementById('lastName').value.trim(),
				email: document.getElementById('email').value.trim().toLowerCase(),
				phone: document.getElementById('phone').value.replace(/\D/g, '')
			};
			
			    console.log('üì§ Sending to Firebase:', formData);
			
			try {
				// ===== CORRECTED API CALL =====
				// URL: operationsManager (consolidated function)
				// Action: captureWebLead (MUST specify this!)
				const response = await fetch('https://us-central1-my-clever-crm.cloudfunctions.net/operationsManager', {
					method: 'POST',
					headers: {'Content-Type': 'application/json'},
					body: JSON.stringify({
						action: 'captureWebLead',
						firstName: formData.firstName,
						lastName: formData.lastName,
						email: formData.email,
						phone: formData.phone
					})
				});
				
				const result = await response.json();
				console.log('‚úÖ Firebase response:', result);
				
				if (response.ok && result.success) {
					console.log('‚úÖ Contact created:', result.contactId);
					
					// ===== REDIRECT TO ENROLLMENT =====
					submitBtn.innerHTML = '‚úÖ Success! Redirecting to enrollment...';
					submitBtn.style.background = '#090';
					
					// Redirect to enrollment form with contactId
					setTimeout(() => {
						window.location.href = `https://myclevercrm.com/complete-enrollment?contactId=${result.contactId}&source=landing_page`;
					}, 1500); // 1.5 second delay so user sees success message
					
				} else {
					throw new Error(result.error || result.message || 'Failed to process request');
				}
				
